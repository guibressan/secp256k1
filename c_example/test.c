#include "secp256k1.h"
#include <assert.h>
#include <stdio.h>
#include <time.h>

struct timespec time_now() {
	struct timespec now;
	clock_gettime(CLOCK_MONOTONIC, &now);
	return now;
}

static long time_since_ns(struct timespec start) {
	struct timespec end = time_now();
	long elapsedS = end.tv_sec - start.tv_sec;
	long elapsedNs = end.tv_nsec - start.tv_nsec;
	elapsedNs += elapsedS * 1000000000;
	return elapsedNs;
}

int count = 50000;

void test_validate_sig() {
	unsigned char pubkey_b[65] = {
		0x04, 0xb0, 0xe2, 0xc8, 0x79, 0xe4, 0xda, 0xf7, 0xb9, 0xab,
		0x68, 0x35, 0x02, 0x28, 0xc1, 0x59, 0x76, 0x66, 0x76, 0xa1,
		0x4f, 0x58, 0x15, 0x08, 0x4b, 0xa1, 0x66, 0x43, 0x2a, 0xab,
		0x46, 0x19, 0x8d, 0x4c, 0xca, 0x98, 0xfa, 0x3e, 0x99, 0x81,
		0xd0, 0xa9, 0x0b, 0x2e, 0xff, 0xc5, 0x14, 0xb7, 0x62, 0x79,
		0x47, 0x65, 0x50, 0xba, 0x36, 0x63, 0xfd, 0xca, 0xff, 0x94,
		0xc3, 0x84, 0x20, 0xe9, 0xd5,
	};
	unsigned char sighash_b[32] = {
		0x11, 0x74, 0x3b, 0x22, 0x0e, 0x9e, 0x24, 0xe8, 0x9a, 0xbd,
		0x4f, 0xf1, 0x24, 0xa2, 0x74, 0x05, 0x31, 0xfe, 0x7d, 0x7f,
		0x9b, 0x4e, 0x26, 0xde, 0x14, 0x71, 0x0a, 0x53, 0x2f, 0xd5,
		0x43, 0xe2,
	};
	unsigned char sig_b[64] = {
		0xd2, 0x34, 0x59, 0xd0, 0x3e, 0xd7, 0xe9, 0x51, 0x1a, 0x47,
		0xd1, 0x32, 0x92, 0xd3, 0x43, 0x0a, 0x04, 0x62, 0x7d, 0xe6,
		0x23, 0x5b, 0x6e, 0x51, 0xa4, 0x0f, 0x9c, 0xd3, 0x86, 0xf2,
		0xab, 0xe3, 0xe7, 0xd2, 0x5b, 0x08, 0x0f, 0x0b, 0xb8, 0xd8,
		0xd5, 0xf8, 0x78, 0xbb, 0xa7, 0xd5, 0x4a, 0xd2, 0xfd, 0xa6,
		0x50, 0xea, 0x8d, 0x15, 0x8a, 0x33, 0xee, 0x3c, 0xbd, 0x11,
		0x76, 0x81, 0x91, 0xfd,
	};

	secp256k1_context *ctx = secp256k1_context_create(
			SECP256K1_CONTEXT_NONE);
	secp256k1_pubkey pub;
	assert(secp256k1_ec_pubkey_parse(ctx, &pub, (void *)&pubkey_b, 65));
	secp256k1_ecdsa_signature sig;
	assert(secp256k1_ecdsa_signature_parse_compact(ctx,
		&sig, (void *)&sig_b));
	assert(secp256k1_ecdsa_signature_normalize(ctx, &sig, &sig));

	int ok = 0;
	struct timespec start = time_now();
	for (int i = 0; i < count; i++) {
		ok = secp256k1_ecdsa_verify(ctx, &sig, (void *)&sighash_b,
			&pub);
		assert(ok);
	}
	long elapsed = time_since_ns(start);
	secp256k1_context_destroy(ctx);
	printf("tot %ldns, ops %d, ns/op %ld\n", elapsed, count, elapsed/count);
	printf("ok %d\n", ok);
}

int main() {
	test_validate_sig();
	return 0;
}
